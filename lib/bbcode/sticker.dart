class Sticker extends StatelessWidget {
  final String name;

  const Sticker({
    Key key,
    this.name,
  })  : assert(name != null),
        super(key: key);

  @override
  Widget build(BuildContext context) {
    bool isDark = Theme.of(context).brightness == Brightness.dark;
    return Container(
      margin: EdgeInsets.all(2.0),
      child: Image.asset(
        'assets/stickers/${isDark ? 'dark' : 'light'}/$name.png',
        width: 32.0,
      ),
    );
  }
}
